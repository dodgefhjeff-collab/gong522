typedef struct {
    /* 滤波状态 */
    uint32_t sum;              /* 多次采样累加 */
    uint8_t  sample_cnt;       /* 当前采样计数 */
    int32_t  ema_val;          /* EMA滤波值 (×16 定点) */
    uint8_t  ema_inited;       /* EMA是否已初始化 */

    /* 输出 */
    int16_t  temp_x10;         /* 最终温度值×10 */
    NTC_Status_t status;       /* 故障状态 */

#ifdef NTC_RUNTIME_SELECT
    const int16_t *table;      /* 运行时选择的表 */
#endif
} NTC_Handle_t;

/**
 * @brief 初始化NTC句柄
 */
static inline void NTC_Init(NTC_Handle_t *h)
{
    h->sum = 0;
    h->sample_cnt = 0;
    h->ema_val = 0;
    h->ema_inited = 0;
    h->temp_x10 = 0;
    h->status = NTC_OK;
#ifdef NTC_RUNTIME_SELECT
    h->table = ntc_table_b3435;  /* 默认B3435 */
#endif
}

#ifdef NTC_RUNTIME_SELECT
/**
 * @brief 运行时切换NTC参数
 */
static inline void NTC_SetParam(NTC_Handle_t *h, NTC_Param_t param)
{
    h->table = ntc_runtime_tables[param];
    h->ema_inited = 0;  /* 切换后重置滤波器 */
}
#endif

/**
 * @brief 喂入一个ADC原始值, 内部自动滤波
 * @param h: NTC句柄
 * @param adc_raw: 12-bit ADC原始值
 * @return 当一轮采样完成时返回1, 否则返回0
 *
 * 典型用法 (在ADC中断或DMA回调中):
 *   if (NTC_FeedADC(&ntc, adc_val)) {
 *       int16_t temp = NTC_GetTempX10(&ntc);
 *   }
 */
static inline uint8_t NTC_FeedADC(NTC_Handle_t *h, uint16_t adc_raw)
{
    h->sum += adc_raw;
    h->sample_cnt++;

    if (h->sample_cnt < NTC_FILTER_SAMPLES) {
        return 0;
    }

    /* 多次采样均值 */
    uint16_t adc_avg = (uint16_t)(h->sum >> NTC_FILTER_SHIFT);
    h->sum = 0;
    h->sample_cnt = 0;

    /* 故障检测 */
#ifdef NTC_POSITION_LOWER
    /* 下拉: ADC≈0 -> NTC短路(R→0), ADC≈4095 -> NTC断路(R→∞) */
    if (adc_avg < NTC_ADC_SHORT_TH) { h->status = NTC_ERR_SHORT; h->temp_x10 = NTC_TEMP_ERR_X10; return 1; }
    if (adc_avg > NTC_ADC_OPEN_TH)  { h->status = NTC_ERR_OPEN;  h->temp_x10 = NTC_TEMP_ERR_X10; return 1; }
#else
    /* 上拉: ADC≈4095 -> NTC短路, ADC≈0 -> NTC断路 */
    if (adc_avg > NTC_ADC_OPEN_TH)  { h->status = NTC_ERR_SHORT; h->temp_x10 = NTC_TEMP_ERR_X10; return 1; }
    if (adc_avg < NTC_ADC_SHORT_TH) { h->status = NTC_ERR_OPEN;  h->temp_x10 = NTC_TEMP_ERR_X10; return 1; }
#endif

    /* EMA一阶滤波 (定点: ema_val放大16倍) */
    int32_t adc_fp = (int32_t)adc_avg << 4;
    if (!h->ema_inited) {
        h->ema_val = adc_fp;
        h->ema_inited = 1;
    } else {
        /* ema = ema + (new - ema) >> alpha_shift */
        h->ema_val += (adc_fp - h->ema_val) >> NTC_EMA_ALPHA_SHIFT;
    }
    uint16_t adc_filtered = (uint16_t)((h->ema_val + 8) >> 4);

    /* 查表 + 插值 */
#ifdef NTC_RUNTIME_SELECT
    h->temp_x10 = NTC_LookupInterp(adc_filtered, h->table);
#else
    h->temp_x10 = NTC_LookupInterp(adc_filtered, ntc_sparse_table);
#endif

    h->status = NTC_OK;
    return 1;
}

/**
 * @brief 获取当前温度 (×10)
 */
static inline int16_t NTC_GetTempX10(const NTC_Handle_t *h)
{
    return h->temp_x10;
}

/**
 * @brief 获取浮点温度
 */
static inline float NTC_GetTempFloat(const NTC_Handle_t *h)
{
    return h->temp_x10 / 10.0f;
}

/**
 * @brief 获取故障状态
 */
static inline NTC_Status_t NTC_GetStatus(const NTC_Handle_t *h)
{
    return h->status;
}

/**
 * @brief 简易接口 - 直接ADC转温度 (无滤波, 兼容旧接口)
 */
static inline int16_t Temp_transition(uint16_t adc_data)
{
#ifdef NTC_RUNTIME_SELECT
    return NTC_LookupInterp(adc_data, ntc_table_b3435);
#else
    return NTC_LookupInterp(adc_data, ntc_sparse_table);
#endif
}

static inline int16_t Temp_transition_int(uint16_t adc_data)
{
    return Temp_transition(adc_data) / 10;
}

static inline float Temp_transition_float(uint16_t adc_data)
{
    return Temp_transition(adc_data) / 10.0f;
}

/* ========================================================================== */
/*                 带符号正码表示法 (Sign-Magnitude Encoding)                    */
/* ========================================================================== */
/*
 * 编码格式 (uint16_t):
 *   bit[15]   = 符号位: 0=正温度, 1=负温度
 *   bit[14:0] = 温度绝对值 (单位: 0.1°C)
 *
 * 示例:
 *   +25.0°C -> temp_x10=250  -> 0x00FA
 *   -40.0°C -> temp_x10=-400 -> 0x8190  (0x8000 | 400)
 *    +0.0°C -> temp_x10=0    -> 0x0000
 *   -55.0°C -> temp_x10=-550 -> 0x8226  (0x8000 | 550)
 *  +200.0°C -> temp_x10=2000 -> 0x07D0
 */

#define NTC_SIGN_BIT      0x8000u
#define NTC_MAG_MASK      0x7FFFu

/**
 * @brief temp_x10 -> 带符号正码
 */
static inline uint16_t NTC_TempToCode(int16_t temp_x10)
{
    if (temp_x10 >= 0) {
        return (uint16_t)temp_x10;
    }
    return (uint16_t)(NTC_SIGN_BIT | (uint16_t)(-temp_x10));
}

/**
 * @brief 带符号正码 -> temp_x10
 */
static inline int16_t NTC_CodeToTemp(uint16_t code)
{
    if (code & NTC_SIGN_BIT) {
        return -(int16_t)(code & NTC_MAG_MASK);
    }
    return (int16_t)code;
}

/**
 * @brief ADC直接转带符号正码
 */
static inline uint16_t Temp_transition_code(uint16_t adc_data)
{
    return NTC_TempToCode(Temp_transition(adc_data));
}

/**
 * @brief 从NTC句柄获取带符号正码
 */
static inline uint16_t NTC_GetTempCode(const NTC_Handle_t *h)
{
    return NTC_TempToCode(h->temp_x10);
}

/**
 * @brief 批量ADC转带符号正码
 */
static inline void NTC_BatchConvertCode(const uint16_t *adc_buf, uint16_t *code_buf,
                                         uint16_t len, const int16_t *table)
{
    for (uint16_t i = 0; i < len; i++) {
        code_buf[i] = NTC_TempToCode(NTC_LookupInterp(adc_buf[i], table));
    }
}

/* ========================================================================== */
/*                      DMA批量转换辅助函数                                     */
/* ========================================================================== */

/**
 * @brief 批量ADC转温度 (适用于DMA采集后一次性转换)
 * @param adc_buf: ADC数据缓冲区
 * @param temp_buf: 温度输出缓冲区 (×10)
 * @param len: 数据长度
 * @param table: 查找表指针
 */
static inline void NTC_BatchConvert(const uint16_t *adc_buf, int16_t *temp_buf,
                                     uint16_t len, const int16_t *table)
{
    for (uint16_t i = 0; i < len; i++) {
        temp_buf[i] = NTC_LookupInterp(adc_buf[i], table);
    }
}

