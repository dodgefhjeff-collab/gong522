/**
 * @brief 稀疏表 + 线性插值查温度
 * @param adc_val: 12-bit ADC值 (0~4095)
 * @param table: 稀疏查找表指针 (可用于运行时切换)
 * @return 温度值×10
 */
static inline int16_t NTC_LookupInterp(uint16_t adc_val, const int16_t *table)
{
    if (adc_val > 4095u) adc_val = 4095u;

    uint16_t idx = adc_val >> 2;           /* adc_val / 4 */
    uint16_t frac = adc_val & 0x03u;       /* adc_val % 4 */

    if (idx >= (NTC_TABLE_SIZE - 1u)) {
        return table[NTC_TABLE_SIZE - 1u];
    }

    if (frac == 0u) {
        return table[idx];
    }

    /* 线性插值: t = t0 + (t1 - t0) * frac / 4 */
    int16_t t0 = table[idx];
    int16_t t1 = table[idx + 1u];
    return (int16_t)(t0 + (((int32_t)(t1 - t0) * frac + 2) >> 2));
}

