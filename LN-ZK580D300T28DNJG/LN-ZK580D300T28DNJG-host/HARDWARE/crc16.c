//                功能：帧CRC-16的计算 (高低字节分别查表法)                           //
//  参数说明：frame数据缓存区
//			  len数据总长度，待校验数据长度len-2，最后两个字节为crc16                  //

void CRC16_build(unsigned char *frame, unsigned int len)
{
    unsigned char *crcptr;
    unsigned char crc_hi,crc_lo,byte_temp;
    //unsigned short crc16Result;
    
    int i;

    crc_hi=0x00;
    crc_lo=0x00;
    crcptr=frame;	    //数据起始地址
    for(i=0; i<(len-2); i++)
    {
        byte_temp=crc_hi ^ *(crcptr+i);
        crc_hi=crc_lo ^ *(CRC16_hi_lut+byte_temp);
        crc_lo=*(CRC16_lo_lut+byte_temp);
    }
    frame[len-2]=crc_hi;
    frame[len-1]=crc_lo;
}

