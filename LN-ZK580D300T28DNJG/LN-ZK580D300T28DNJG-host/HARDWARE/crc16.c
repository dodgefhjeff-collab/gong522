unsigned short CRC16_8005(unsigned char *data,unsigned int offset,unsigned int len)
{
    
    unsigned int i = 0;
    unsigned char J,G1,GO;
    
    unsigned short crc = 0;

    J = data[offset++];
    G1 = data[offset++];
    GO = data[offset++];

    for(i = 0; i < len; i++)
    {
        GO = (unsigned char)(GO ^ cs0[(J & 0xff) * 2]);
        J = (unsigned char)(G1 ^ cs0[(J & 0xff) * 2 + 1]);
        G1 = GO;
        GO = (i < len -3) ? data[offset++] : 0;
    }
    crc = (unsigned short)((G1 & 0xff) * 256 + (J & 0xff));
    
    return crc;
}

unsigned int Add_SLIP(unsigned int *data,unsigned int datalen)
{
    
    unsigned char ntemp[200];
    
    unsigned int i = 0;
    
    unsigned int k = 0;
    for(i = 0; i < datalen; i++)
    {
        ntemp[i] = data[i];
    }
    data[k] = 0xc0;
    k++;
    for(i = 0; i < datalen; i++)
    {
        if(ntemp[i] == 0xc0)
        {
            data[k] = 0xdb;
            k++;
            data[k] = 0xdc;
            k++;
        }
        else if(ntemp[i] == 0xdb)
        {
            data[k] = 0xdb;
            k++;
            data[k] = 0xdd;
            k++;
        }
        else
        {
            data[k] = ntemp[i];
            k++;
        }
    }
    data[k] = 0xc0;
    k++;
    
    return k;//加上SLIP之后的数据长度
}

